int32_t gmt2local(time_t);





int32_t
gmt2local(time_t t)
{
 int dt, dir={0};
 struct tm *gmt, *loc={0};
 struct tm sgmt={0};

 if (t == 0)
  t = time(
          ((void *)0)
              );
 gmt = &sgmt;
 *gmt = *gmtime(&t);
 loc = localtime(&t);
 dt = (loc->tm_hour - gmt->tm_hour) * 60 * 60 +
     (loc->tm_min - gmt->tm_min) * 60;






 dir = loc->tm_year - gmt->tm_year;
 if (dir == 0)
  dir = loc->tm_yday - gmt->tm_yday;
 dt += dir * 24 * 60 * 60;

 return (dt);
}
