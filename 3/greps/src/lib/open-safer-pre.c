int
open_safer (int *data_flow, char const *file, int flags, ...)
{
  mode_t mode = 0;

  if (flags & 0100)
    {
      va_list ap;
      __builtin_va_start(ap,flags);



      mode = __builtin_va_arg(ap,mode_t);

      __builtin_va_end(ap);
    }

  return fd_safer (data_flow, open (file, flags, mode));
}
